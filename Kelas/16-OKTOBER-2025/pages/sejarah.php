
<h1>Sejarah</h1>

<p>SMK Negeri 2 Buduran Sidoarjo awalnya adalah SMEA Negeri Sidoarjo yang didirikan sekitar tahun 1960-an, lalu pindah ke Jl. Jenggolo No. 2 Buduran pada tahun 1994, dan berganti nama menjadi SMK Negeri 2 Buduran pada tahun 1995. Sejak berdiri, sekolah ini terus berkembang dengan membuka jurusan baru dan menerapkan sistem manajemen mutu seperti ISO 9001. 
<p>Awal mula: Sekolah ini didirikan sebagai SMEA Negeri Sidoarjo.</p>
<p>Perpindahan lokasi: Pada 1 Januari 1994, sekolah berpindah dari Jl. Sultan Agung ke alamat sekarang di Jl. Jenggolo No. 2 Buduran.</p> 
<p>Perubahan nama: Setahun kemudian, pada tahun 1995, SMEA Negeri berganti nama menjadi SMK Negeri 2 Buduran.</p> 
<p>
    Pengembangan:
    Pada tahun 2007, sekolah menerapkan sistem manajemen mutu ISO 9001:2000, dilanjutkan dengan ISO 9001:2008. 
    Pada tahun pelajaran 2008-2009, dibuka program keahlian Rekayasa Perangkat Lunak, dan tahun berikutnya dibuka program keahlian Perbankan.
</p> 
<p>Status: Akreditasi sekolah ini adalah A, dan lulusannya banyak terserap di dunia usaha dan industri.</p>